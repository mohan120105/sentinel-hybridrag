"""Initialize Sentinel ontology and ingest policy documents into Neo4j.

This script bootstraps the foundational graph for Sentinel GraphRAG by:
1) Creating a fixed SME-governed ontology of policy categories.
2) Generating synthetic banking policy documents.
3) Running Curator extraction via Groq with strict Pydantic output.
4) Persisting policies and supersession chains into Neo4j.
"""

