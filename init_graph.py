class GraphAction(BaseModel):
    """Structured graph mutation action generated by Curator extraction.

    Business logic:
    - target_node binds policy to a fixed SME-governed ontology category.
    - action_type controls whether this policy is a new leaf or supersedes prior guidance.
    - extracted_rule captures the actionable compliance rule for retrieval-time evidence.
    - superseded_document names prior policy to connect supersession lineage.
    """

    target_node: TargetCategory = Field(
        ..., description="Ontology category that the document belongs to."
    )
    action_type: ActionType = Field(
        ..., description="CREATE_NEW for fresh guidance, SUPERSEDE_OLD for replacements."
    )
    extracted_rule: str = Field(
        ..., min_length=10, description="Concise compliance rule extracted from the document."
    )
    superseded_document: Optional[str] = Field(
        default=None,
        description="Name of old policy being replaced when action_type is SUPERSEDE_OLD.",
    )
    applies_to_customer: List[str] = Field(
        default_factory=list,
        description="Customer segments this policy applies to (for APPLIES_TO edges).",
    )
    requires_document: List[str] = Field(
        default_factory=list,
        description="Required documents/collateral for this policy (for REQUIRES edges).",
    )


def initialize_ontology(driver: Driver) -> None:
    """Reset database and create fixed ontology category nodes.

    This function creates the SME-governed root ontology to ensure every ingested
    policy maps to one canonical business category.
    """

    clear_query = "MATCH (n) DETACH DELETE n"
    create_categories_query = """
    UNWIND $categories AS category_name
    MERGE (:Category {name: category_name})
    """

    try:
        with driver.session() as session:
            session.execute_write(lambda tx: tx.run(clear_query).consume())
            session.execute_write(
                lambda tx: tx.run(
                    create_categories_query,
                    categories=list(CATEGORY_VALUES),
                ).consume()
            )
        print("Ontology initialized with Category nodes.")
    except ServiceUnavailable as error:
        print(f"Neo4j connection dropped while initializing ontology: {error}")
        raise
    except Neo4jError as error:
        print(f"Neo4j error while initializing ontology: {error}")
        raise
    except Exception as error:
        print(f"Unexpected error while initializing ontology: {error}")
        raise


def create_policy_vector_index(driver: Driver, embedding_dimensions: int) -> None:
    """Create Neo4j vector index for semantic policy retrieval."""

    create_index_query = """
    CREATE VECTOR INDEX policy_embeddings IF NOT EXISTS
    FOR (n:Policy) ON (n.embedding)
    OPTIONS {
      indexConfig: {
        `vector.dimensions`: $dimensions,
        `vector.similarity_function`: 'cosine'
      }
    }
    """

    try:
        with driver.session() as session:
            session.execute_write(
                lambda tx: tx.run(
                    create_index_query,
                    dimensions=embedding_dimensions,
                ).consume()
            )
        print(
            "Vector index ready: policy_embeddings "
            f"(dimensions={embedding_dimensions}, similarity=cosine)."
        )
    except ServiceUnavailable as error:
        print(f"Neo4j connection dropped while creating vector index: {error}")
        raise
    except Neo4jError as error:
        print(f"Neo4j error while creating vector index: {error}")
        raise


def create_policy_fulltext_index(driver: Driver) -> None:
    """Create Neo4j full-text index for keyword-backed hybrid retrieval."""

    create_index_query = """
    CREATE FULLTEXT INDEX policy_keywords IF NOT EXISTS
    FOR (n:Policy)
    ON EACH [n.name, n.extracted_rule, n.source_text]
    """

    try:
        with driver.session() as session:
            session.execute_write(lambda tx: tx.run(create_index_query).consume())
        print("Full-text index ready: policy_keywords (name, extracted_rule, source_text).")
    except ServiceUnavailable as error:
        print(f"Neo4j connection dropped while creating full-text index: {error}")
        raise
    except Neo4jError as error:
        print(f"Neo4j error while creating full-text index: {error}")
        raise


