def process_and_ingest(
    driver: Driver,
    llm: ChatGroq,
    embeddings_model: HuggingFaceEndpointEmbeddings,
    documents: List[Dict[str, str]],
) -> None:
    """Extract graph actions with Groq and write policy lineage into Neo4j.

    Business logic:
    - Creates one Policy node per source document and links it to Category via BELONGS_TO.
    - On SUPERSEDE_OLD, updates supersession chain by creating SUPERSEDES edge from
      new policy to prior policy and marks old policy inactive.
    """

    structured_llm = llm.with_structured_output(GraphAction)

    create_policy_query = """
    MATCH (c:Category {name: $category_name})
    CREATE (p:Policy {
        name: $policy_name,
        issue_date: $issue_date,
        source_text: $source_text,
        extracted_rule: $extracted_rule,
        embedding: $embedding,
        action_type: $action_type,
        access_code: $access_code,
        active: true,
        created_at: datetime($created_at)
    })
    MERGE (p)-[:BELONGS_TO]->(c)
    WITH p
    UNWIND (CASE WHEN size($applies_to_customer) > 0 THEN $applies_to_customer ELSE [null] END) AS customer
    FOREACH (ignoreMe IN CASE WHEN customer IS NOT NULL THEN [1] ELSE [] END |
        MERGE (ct:CustomerType {name: customer})
        MERGE (p)-[:APPLIES_TO]->(ct)
    )
    WITH p
    UNWIND (CASE WHEN size($requires_document) > 0 THEN $requires_document ELSE [null] END) AS doc
    FOREACH (ignoreMe IN CASE WHEN doc IS NOT NULL THEN [1] ELSE [] END |
        MERGE (dr:DocumentRequirement {name: doc})
        MERGE (p)-[:REQUIRES]->(dr)
    )
    RETURN p.name AS created_policy
    """

    supersede_query = """
    MATCH (new_policy:Policy {name: $new_policy_name})
    MATCH (old_policy:Policy {name: $old_policy_name})
    MERGE (new_policy)-[:SUPERSEDES]->(old_policy)
    SET old_policy.active = false,
        old_policy.retired_at = datetime($retired_at)
    RETURN new_policy.name AS new_policy_name, old_policy.name AS old_policy_name
    """

    for document in documents:
        document_name = document["name"]
        try:
            prompt = CURATOR_PROMPT_TEMPLATE.format(
                categories=", ".join(CATEGORY_VALUES),
                document_name=document_name,
                issue_date=document["issue_date"],
                document_text=document["text"],
            )

            action: GraphAction = structured_llm.invoke(prompt)
            semantic_text = f"{action.extracted_rule}\n\n{document['text']}"
            question_embedding: Sequence[float] = embeddings_model.embed_query(semantic_text)
            embedding = [float(value) for value in question_embedding]
            timestamp = datetime.utcnow().isoformat()

            with driver.session() as session:
                creation_result = session.execute_write(
                    lambda tx: tx.run(
                        create_policy_query,
                        category_name=action.target_node,
                        policy_name=document_name,
                        issue_date=document["issue_date"],
                        source_text=document["text"],
                        extracted_rule=action.extracted_rule,
                        embedding=embedding,
                        action_type=action.action_type,
                        access_code=DEFAULT_ACCESS_CODE,
                        applies_to_customer=action.applies_to_customer,
                        requires_document=action.requires_document,
                        created_at=timestamp,
                    ).single(strict=True)
                )

                print(
                    "Ingested policy:",
                    creation_result["created_policy"],
                    "| category:",
                    action.target_node,
                    "| action:",
                    action.action_type,
                )

                if (
                    action.action_type == "SUPERSEDE_OLD"
                    and action.superseded_document is not None
                ):
                    supersede_result = session.execute_write(
                        lambda tx: tx.run(
                            supersede_query,
                            new_policy_name=document_name,
                            old_policy_name=action.superseded_document,
                            retired_at=timestamp,
                        ).single()
                    )

                    if supersede_result is None:
                        print(
                            "Warning: superseded document not found for",
                            f"{document_name} -> {action.superseded_document}",
                        )
                    else:
                        print(
                            "Supersession linked:",
                            f"{supersede_result['new_policy_name']} -> "
                            f"{supersede_result['old_policy_name']}",
                        )
        except ServiceUnavailable as error:
            print(f"Neo4j connection dropped while ingesting {document_name}: {error}")
        except Neo4jError as error:
            print(f"Neo4j error while ingesting {document_name}: {error}")
        except ValidationError as error:
            print(f"Structured output validation failed for {document_name}: {error}")
        except Exception as error:
            print(f"LLM/parsing error while ingesting {document_name}: {error}")


def build_neo4j_driver() -> Driver:
    """Create Neo4j driver from environment configuration."""

    uri = os.getenv("NEO4J_URI")
    user = os.getenv("NEO4J_USER") or os.getenv("NEO4J_USERNAME")
    password = os.getenv("NEO4J_PASSWORD")

    if not uri or not user or not password:
        raise ValueError("NEO4J_URI, NEO4J_USER/NEO4J_USERNAME, and NEO4J_PASSWORD must be set.")

    return GraphDatabase.driver(uri, auth=(user.strip(), password.strip()))


def build_groq_llm() -> ChatGroq:
    """Create Groq chat model client for structured Curator extraction."""

    api_key = os.getenv("GROQ_API_KEY")
    if not api_key:
        raise ValueError("GROQ_API_KEY is not set. Export it before running this script.")

    return ChatGroq(
        model="llama-3.3-70b-versatile",
        api_key=api_key,
        temperature=0,
    )


def build_embeddings_model() -> HuggingFaceEndpointEmbeddings:
    """Create cloud sentence-transformer embeddings client for policy vectors."""

    hf_token = os.getenv("HF_TOKEN")
    if not hf_token:
        raise ValueError("HF_TOKEN is not set. Export it before running this script.")

    return HuggingFaceEndpointEmbeddings(
        model="sentence-transformers/all-MiniLM-L6-v2",
        task="feature-extraction",
        huggingfacehub_api_token=hf_token,
    )


def main() -> None:
    """Initialize ontology and ingest mock policies into graph."""

    driver = build_neo4j_driver()
    driver.verify_connectivity()
    try:
        llm = build_groq_llm()
        embeddings_model = build_embeddings_model()
        embedding_dimensions = len(embeddings_model.embed_query("dimension_probe"))
        initialize_ontology(driver)
        create_policy_vector_index(driver, embedding_dimensions)
        create_policy_fulltext_index(driver)
        documents = get_mock_documents()
        process_and_ingest(driver, llm, embeddings_model, documents)
    finally:
        driver.close()


if __name__ == "__main__":
    main()
